#include <stdio.h>

int main()
{
    int m1[5][5] = {{0, 1, 2, 3, 4}, {4, 5, 6, 7, 6}, {8, 4, 5, 0, 5}, {6, 0, 2, 1, 8}, {5, 6, 1, 2, 2}};
    int m2[5][5] = {{4, 5, 6, 7, 4}, {0, 2, 3, 1, 0}, {8, 4, 5, 0, 4}, {6, 0, 2, 1, 8}, {5, 6, 1, 2, 2}};
    int m3[5][5];

    for (int i = 0; i < 5; i++)
        for (int j = 0; j < 5; j++)
            m3[i][j] = m1[i][j] + m2[i][j];

    for (int i = 0; i < 5; i++)
    {
        for (int j = 0; j < 5; j++)
            printf("%d ", m3[i][j]);
        printf("\n");
    }

    return 0;
}
